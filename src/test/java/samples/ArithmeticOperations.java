public class ArithmeticOperations {
    //Functionality 1
    public int addition(int operand1, int operand2){
        return operand1 + operand2;
    }

    //Functionality 2
    public int multiplication(int operand1, int operand2){
        return  operand1 * operand2;
    }
}
